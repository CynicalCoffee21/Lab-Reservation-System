<h1 class="section-head">Lab Policies and FAQs</h1>
<div class="text-mod-txt contenteditable">
<div class="spotlight-box">
<h2 class="spotlight-group_heading"><br/></h2>
<div class="spotlight-txt">
<h3 style="text-align:center">Equipment Checkout for&nbsp;Instructors</h3>
<p>Instructors interested in checking&nbsp;out response clickers, portable projectors,&nbsp;screens&nbsp;or laptops should see a lab assistant in Tompkins 113 for details.</p>
</div>
</div>
<h3>General Lab Policies:</h3>
<h5>Food and Drinks</h5>
<p style="margin-left:20px">Students can have food and drink in the lab, but the items must be covered. Please be sure to clean your workspace before leaving the lab.</p>
<h5>Installation of Software</h5>
<p style="margin-left:20px">The installation of software is strictly prohibited. We have gone to a lot of trouble to configure these machines and provide our users with as wide an array of software as we can afford. The installation of certain programs can conflict with our own networking software and render the computer unusable by other students. Anyone caught installing outside software will be ejected from the labs, and a permanent ban will be considered.</p>
<h5>Where to Save Your Work</h5>
<p style="margin-left:20px">We ask that our users save their work to either removable media, such as a USB flash drive, or to your Unity/AFS account space (K: drive). User files are deleted automatically once you log off. If you save anything to the desktop it will be gone the next time you log in!</p>
<h5>No alternative paper</h5>
<p style="margin-left:20px">Printing is only allowed on white 8.5x11" letter-sized paper; we do not allow our printers to process any alternate types of paper.</p>
<h5>Wolfcopy Printers</h5>
<p style="margin-left:20px">There are black/white and color Wolfcopy printers in all labs. Both of these printers require print quota. There are no restrictions on what can be printed or the number of copies on these printers, just make sure to have enough print quota.&nbsp;​</p>
</div>
